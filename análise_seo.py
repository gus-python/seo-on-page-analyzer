import requests
from bs4 import BeautifulSoup

def analisador_URL (palavra):
  pontos = 0
  URL = input('Cole a URL: ')
  resultados = {}
  for urls in URL:
    urls = urls.strip()
    response = requests.get(urls)
    soup = BeautifulSoup(response.text, 'html.parser')
    h1 = soup.find('h1')
    if h1:
      pontos += 15
      texto_h1 = h1.get_text().lower()
      status_h1 = f'Sim'
      if palavra in texto_h1:
        pontos += 15
        palavra_h1 = f'Sim'
      else:
          palavra_h1 = 'Não'
      if palavra in texto_h1 [:30]:
        pontos += 10
        inicio_h1 = 'Sim'
      else:
          inicio_h1 = 'Não'
    else:
        status_h1 = 'Não'

    resultados[urls] = {
        'H1 encontrado?': status_h1,
        'Palavra-chave no H1?': palavra_h1,
        'Palavra-chave no início do H1?' : inicio_h1,}
  for enderecos, dados in resultados.items():
    print('\nURL:',enderecos)
    print('\nAnalise:',dados)
    return pontos
